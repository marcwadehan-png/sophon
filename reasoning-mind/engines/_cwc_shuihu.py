def get_recommend_shuihu_case(matched_themes: List[str]) -> Dict[str, Any]:
    """根据匹配主题推荐水浒案例"""
    case_map = {
        "团队": "宋江题反诗",
        "困境": "林冲被逼上梁山",
        "义气": "鲁智深野猪林救林冲",
        "领导": "招安悲剧",
        "团队建设": "宋江题反诗"
    }

    case_names = [case_map.get(t, "武松打虎") for t in matched_themes[:1]]
    wisdom = get_shuihu_wisdom()

    return {
        "case_name": case_names[0] if case_names else "武松打虎",
        "case_detail": wisdom["classic_cases"].get(
            case_names[0] if case_names else "武松打虎",
            wisdom["classic_cases"]["武松打虎"]
        )
    }

def summarize_shuihu_wisdom(matched_themes: List[str]) -> str:
    """总结水浒智慧"""
    wisdom = get_shuihu_wisdom()
    return (
        f"水浒智慧揭示团队建设真谛：{', '.join(wisdom['team_wisdom'].keys())}。"
        f"核心教训：{wisdom['leadership_lessons'][0]}，"
        f"警示：{wisdom['leadership_lessons'][-1]}。"
    )

def generate_shuihu_actions(matched_themes: List[str]) -> List[str]:
    """生成水浒视角的行动建议"""
    actions = [
        "明确团队愿景，避免目标迷失",
        "建立合理的权力结构和利益分配机制",
        "包容团队成员的缺点，发挥各自优势",
        "关键时刻要勇于担当",
        "维护团队凝聚力和忠诚度"
    ]

    if "团队" in matched_themes:
        actions.insert(0, "明确团队使命，确保成员方向一致")
    if "困境" in matched_themes:
        actions.insert(0, "分析困境根源，寻找突破口")

    return actions[:5]
