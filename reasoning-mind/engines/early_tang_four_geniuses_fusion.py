    def 分析wenben(self, 文本: str) -> Dict[str, Any]:
        """
        分析给定文本是否符合四杰的style

        参数:
            文本: 待分析的诗歌文本

        返回:
            分析结果字典
        """
        # 调用各个引擎分析
        王勃结果 = self.王勃.分析文本(文本)
        骆宾王结果 = self.骆宾王.分析文本(文本)

        # 关键词匹配
        四杰关键词 = {
            "王勃": ["落霞", "秋水", "知己", "比邻", "滕王阁"],
            "骆宾王": ["蝉", "露重", "高洁", "从军"]
        }

        # 计算匹配度
        王勃匹配 = sum(1 for k in 四杰关键词["王勃"] if k in 文本)
        骆宾王匹配 = sum(1 for k in 四杰关键词["骆宾王"] if k in 文本)

        # judge最可能的poet
        if 王勃匹配 > 骆宾王匹配:
            最可能poet = "王勃"
            匹配度 = 王勃匹配 / len(四杰关键词["王勃"])
            style类型 = 王勃结果["style类型"]
        else:
            最可能poet = "骆宾王"
            匹配度 = 骆宾王匹配 / len(四杰关键词["骆宾王"])
            style类型 = 骆宾王结果["style类型"]

        return {
            "引擎": self.引擎名称,
            "文本长度": len(文本),
            "四杰characteristics匹配": {
                "王勃": 王勃匹配,
                "骆宾王": 骆宾王匹配
            },
            "最可能poet": 最可能poet,
            "匹配度": min(匹配度, 1.0),
            "style类型": style类型,
            "分析结果": {
                "王勃": 王勃结果,
                "骆宾王": 骆宾王结果
            }
        }

