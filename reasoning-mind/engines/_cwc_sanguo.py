def get_recommend_sanguo_case(matched_themes: List[str]) -> Dict[str, Any]:
    """根据匹配主题推荐三国案例"""
    case_map = {
        "竞争": "赤壁之战",
        "人才": "三顾茅庐",
        "战略": "隆中对",
        "领导": "刘备的领导艺术",
        "战略规划": "隆中对"
    }

    case_names = [case_map.get(t, "官渡之战") for t in matched_themes[:1]]
    wisdom = get_sanguo_wisdom()

    return {
        "case_name": case_names[0] if case_names else "官渡之战",
        "case_detail": wisdom["strategic_cases"].get(
            case_names[0] if case_names else "官渡之战",
            wisdom["strategic_cases"]["官渡之战"]
        )
    }

def summarize_sanguo_wisdom(matched_themes: List[str]) -> str:
    """总结三国智慧"""
    wisdom = get_sanguo_wisdom()
    return (
        f"三国智慧强调：{', '.join(wisdom['core_principles'].keys())}。"
        f"竞争策略：{wisdom['winning_keys'][0]}，"
        f"人才观念：{wisdom['core_principles']['卧龙凤雏得一可安天下']}。"
    )

def generate_sanguo_actions(matched_themes: List[str]) -> List[str]:
    """生成三国视角的行动建议"""
    actions = [
        "顺应大势，把握战略时机",
        "重视人才，招揽核心成员",
        "建立竞争优势，寻找差异化定位",
        "善用联盟，分化对手",
        "保持团队凝聚力"
    ]

    if "人才" in matched_themes:
        actions.insert(0, "深入了解团队成员优势，因材施用")
    if "竞争" in matched_themes:
        actions.insert(0, "分析竞争对手弱点，寻找突破点")

    return actions[:5]
