    def _recognizestyle(self, 词句: str) -> List[柳永词风类型]:
        """recognize词句的style类型"""
        style结果 = []
        
        # 羁旅行役词recognize
        羁旅词 = ["离别", "漂泊", "游子", "思乡", "孤", "客", "旅", "行役", "天涯", "异乡"]
        if any(词 in 词句 for 词 in 羁旅词):
            style结果.append(柳永词风类型.羁旅行役)
        
        # 都市繁华词recognize
        都市词 = ["繁华", "都会", "市井", "街", "市", "城", "珠玑", "罗绮", "形胜"]
        if any(词 in 词句 for 词 in 都市词):
            style结果.append(柳永词风类型.都市繁华)
        
        # 青楼情思词recognize
        青楼词 = ["相思", "憔悴", "伊", "情", "爱", "恋", "红袖", "青楼", "歌妓"]
        if any(词 in 词句 for 词 in 青楼词):
            style结果.append(柳永词风类型.青楼情思)
        
        # 节令风俗词recognize
        节令词 = ["清明", "元宵", "中秋", "重阳", "寒食", "春节", "时令", "节气"]
