    def 分析wenben(self, 文本: str) -> Dict[str, Any]:
        """
        分析给定文本是否符合岑参的style

        参数:
            文本: 待分析的诗歌文本

        返回:
            分析结果字典
        """
        # 关键词匹配
        奇景关键词 = ["雪", "风", "火", "云", "冰", "霜", "热", "寒"]
        异域关键词 = ["胡", "羌", "胡琴", "琵琶", "羌笛"]
        送别关键词 = ["送", "别", "归", "逢", "别"]
        功名关键词 = ["功名", "马上", "英雄", "青史"]
        夸张关键词 = ["千树", "万树", "大如斗", "火云"]

        奇景得分 = sum(1 for k in 奇景关键词 if k in 文本)
        异域得分 = sum(1 for k in 异域关键词 if k in 文本)
        送别得分 = sum(1 for k in 送别关键词 if k in 文本)
        功名得分 = sum(1 for k in 功名关键词 if k in 文本)
        夸张得分 = sum(1 for k in 夸张关键词 if k in 文本)

        总分 = 奇景得分 + 异域得分 + 送别得分 + 功名得分

        # judge_style类型
        if 夸张得分 >= 1:
            style类型 = 岑参style类型.边塞奇景
        elif 奇景得分 >= 3:
            style类型 = 岑参style类型.边塞奇景
        elif 异域得分 >= 2:
            style类型 = 岑参style类型.异域风情
        elif 送别得分 >= 2:
            style类型 = 岑参style类型.送别深情
        else:
            style类型 = 岑参style类型.即景抒情

        # judge心境
        if 功名得分 >= 1:
            心境类型 = 岑参心境类型.功名热望
        elif 异域得分 >= 2:
            心境类型 = 岑参心境类型.异域新奇
        elif 送别得分 >= 2:
            心境类型 = 岑参心境类型.思乡怀人
        else:
            心境类型 = 岑参心境类型.以身许国

        return {
            "poet": self.poet名称,
            "style匹配度": min(总分 / 8, 1.0),
            "style类型": style类型.value,
            "心境类型": 心境类型.value,
            "关键词匹配": {
                "边塞奇景": 奇景得分,
                "异域风情": 异域得分,
                "送别深情": 送别得分,
                "功名热望": 功名得分,
                "奇特夸张": 夸张得分
            },
            "建议": f"这首诗体现了岑参{style类型.value}的特点,心境为{心境类型.value}"
        }

