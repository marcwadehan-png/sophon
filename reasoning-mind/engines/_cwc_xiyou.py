def get_recommend_xiyou_case(matched_themes: List[str]) -> Dict[str, Any]:
    """根据匹配主题推荐西游案例"""
    wisdom = get_xiyou_wisdom()
    case_map = {
        "成长": ("成长阶段", "growth_stages"),
        "团队": ("团队角色", "pilgrim_team"),
        "挑战": ("八十一难", "81_trials"),
        "坚持": ("成功要素", "success_factors"),
        "成长与修行": ("成长阶段", "growth_stages")
    }

    theme_key = matched_themes[0] if matched_themes else "成长"
    if theme_key in case_map:
        key, sub_key = case_map[theme_key]
        return {
            "case_name": key,
            "case_detail": wisdom[sub_key]
        }

    return {
        "case_name": "取经团队",
        "case_detail": wisdom["pilgrim_team"]
    }

