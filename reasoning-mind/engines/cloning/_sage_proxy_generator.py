def get_personalized_wisdom_laws(
    sage_name: str,
    school: str,
    p1_laws: List[str] = None,
    p2_core_methods: List[str] = None,
    count: int = 6
) -> List[str]:
    """
    获取贤者的个性化智慧法则
    
    优先级: P1个性化法则 > P2 core_methods > 通用学派模板
    
    Args:
        sage_name: 贤者名称
        school: 学派
        p1_laws: P1蒸馏文档中的智慧法则
        p2_core_methods: P2编码中的核心方法
        count: 返回数量
    
    Returns:
        个性化智慧法则列表
    """
    if p1_laws:
        return p1_laws[:count]
    if p2_core_methods:
        return p2_core_methods[:count]
    return get_wisdom_laws(school, count)


def get_personalized_capability(
    sage_name: str,
    school: str,
    p2_cognitive_dims: Dict[str, float] = None
) -> Dict[str, float]:
    """
    获取贤者的个性化能力值
    
    优先级: P2认知维度 > 通用学派默认值
    
    Args:
        sage_name: 贤者名称
        school: 学派
        p2_cognitive_dims: P2编码中的认知维度评分
    
    Returns:
        能力值字典
    """
    if p2_cognitive_dims:
        dim_to_cap = {
            "cog_depth": "strategic_vision",
            "decision_quality": "execution",
            "value_judge": "ethical_judgment",
            "gov_decision": "governance",
            "strategy": "innovation",
            "self_mgmt": "leadership",
        }
        capability = {}
        for dim_key, cap_key in dim_to_cap.items():
            val = p2_cognitive_dims.get(dim_key)
            if val is not None:
                capability[cap_key] = int(val)
        # 补充剩余维度
        for extra_key in ["dialectical_reasoning", "practical_wisdom",
                          "long_term_vision", "crisis_response",
                          "system_thinking", "pattern_recognition",
                          "communication", "conflict_resolution"]:
            if extra_key not in capability:
                capability[extra_key] = 7
        return capability
    return get_capability_default(school)
